<p>
    Громадська рада при Національному агентстві з питань виявлення, розшуку та управління активами,
    одержаними від корупційних та інших злочинів (АРМА) формується у складі дев’яти осіб
    на підставі результатів конкурсу.

    Конкурс з формування персонального складу Громадської ради проводиться шляхом відкритого
    рейтингового інтернет-голосування громадян, які проживають на території України.
</p>

