    відбудеться на офіційному веб-сайті Національного агентства (www.arma.gov.ua)
    15 лютого 2018 року, протягом 12 годин, <b>з 09.00 години до 21.00 години</b>.
</p>

<p>
    Будь-який громадянин, який проживає на території України, може взяти участь в інтернет-голосуванні.
    Під час участі в інтернет-голосуванні громадянин може проголосувати не більш як за дев’ять кандидатів.
</p>

<p>
