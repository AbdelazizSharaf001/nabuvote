</p>

<p>
    З метою запобігання повторного голосування, бажаючим взяти участь в рейтинговому Інтернет-голосуванні
	необхідно буде підтвердити адресу своєї електронної пошти та номер мобільного телефону
    (українського GSM оператора). На проходження перевірок і обрання кандидатів відводиться
    <b>15 хвилин</b>. Якщо Ви не встигли проголосувати — спробу можна повторити за 5 хвилин.
</p>

<p>
    Наказ про затвердження складу Громадської ради разом з оголошенням про результати інтернет-голосування
    із зазначенням кількості голосів, поданих за кожного кандидата, оприлюднюються на веб-сайті,
    на якому проводилось інтернет-голосування, на наступний день після його видання.
</p>

